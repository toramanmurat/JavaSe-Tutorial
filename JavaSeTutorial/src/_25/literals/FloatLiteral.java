package _25.literals;

public class FloatLiteral {

	public static void main(String[] args) {
		float floatVareiable=4.5F;
		float floatVar=0.5f;
		double doubleVar=1.5;
		double doubleVar2=2.5d;
		
		double doubleVar3=6.5f;
		
		System.out.println(floatVareiable);
		System.out.println(floatVar);
		System.out.println(doubleVar);
		System.out.println(doubleVar2);
		System.out.println(doubleVar3);
		
	}
}

